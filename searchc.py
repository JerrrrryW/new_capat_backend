# 打开Excel文件
workbook = xlsxwriter.Workbook('匹配结果.xlsx')
worksheet = workbook.add_worksheet()

# 定义要查找的字符模式
pattern = r'上海'

# 创建一个红色字体格式
red_font_format = workbook.add_format({'color': 'red'})

# 读取原始Excel文件
with open('/Users/gritty/Desktop/workspace/CAPAT_DB/capat/test/20230906_文科实验室用石涛数据.xlsx', 'rb') as file:
    # 创建一个XlsxWriter对象来读取原始文件内容
    xlsx_reader = xlsxwriter.Workbook(file, {'in_memory': True})
    xlsx_sheet = xlsx_reader.sheet_by_index(0)  # 假设工作表在索引0上

    # 遍历原始工作表的行
    for row_idx in range(xlsx_sheet.nrows):
        row = xlsx_sheet.row_values(row_idx)
        new_row = []

        for cell_value in row:
            if cell_value:
                # 在单元格值中查找匹配的字符
                match = re.search(pattern, str(cell_value))
                if match:
                    # 如果找到匹配项，将匹配的字符设置为红色
                    cell_value = re.sub(pattern, lambda x: f'[[red]]{x.group()}[[/red]]', str(cell_value))

                # 将单元格值写入新的Excel文件
                worksheet.write_rich_string(row_idx, len(new_row), cell_value, red_font_format)
                new_row.append(cell_value)

workbook.close()
