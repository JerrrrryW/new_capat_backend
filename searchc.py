# 《虬峰文集》二十卷，清淮南李膦撰，康熙庚辰刊，①
# 今藏国家图书馆典藏部。《虬峰文集》是一部诗文合
# 集 ，卷一赋 ，卷二乐府 ，卷三至卷十三各体诗 ，卷十四
# 至卷二十史论和杂文。所收诗文 ，按岁月先后排序。在
# 此集中，有关石涛的诗文就有 11篇之多分布在各卷 ：
# 《大涤子饷百合》五律一首(卷七 )、《哭大涤子》小律
# 四首 (卷七)、《清湘子六十赋赠》七律二首 (卷九 )、
# 《读大涤子谒陵诗》七律一首(卷九 )、《过石公城东精
# 舍》七绝一首(卷十三)、《赠石公序》(卷十五)、《大涤
# 子传 (有序)》(卷十六 )、《大涤子梦游记》(卷十八 )、
# 《书大涤子所临米颠雨后山手卷后》(卷十九)、《书吴
# 氏所藏大涤子画后》(卷十九 )、《大涤子谒陵诗 跋》
# (卷 十九 )。
# 此外 ，与石涛有一些关联的诗文还有《挽八大山
# 人》(卷九)、《寓兴}-十三首之一(卷十三 )以及《吴吉
# 人诗序》(卷十五 )、《书笑错子 <踏雪图 >后》(卷十
# 九 )等 。这些诗文 ，对于了解和研究石涛的身世、生平
# 及艺术成就等 ，起到了一定程度 的积极作用 ，并具有
# 一 定 的参考 价值 。但其 中也有舛误之处 ，如 ：记录石涛
# 生年年岁有矛盾 ；记录石涛始入佛门的地点有误；记
# 录石涛云游访道的行踪有误 ；《哭大涤子》有疑；等等。
# 可是 ，《虬峰文集》自康熙庚辰刊刻于世至现在，已有
# 三百多年历史，却很少有人针对此集有关石涛诗文 中
# 的舛误撰写专文提出批评。这也许跟此集在乾隆时被
# 列为禁毁之书而传世极少有关。民国二十五年(1936
# 年 )，孙殿起先生在其《贩书偶记》卷十四中只著录此
# 集 ；1965年 ，邓之诚先生在其《清诗纪事初编》卷一中
# 亦只提 到“膦有虬 峰集 ”。两位先生 均未提 及此集 中是
# 否有误 。到了 1979年 ，汪世清先生在 国家 图书馆善本
# 部查到此集 ，经过一番研究 ，撰成《(虬峰文集)中有关
# 石涛的诗文》，并将其发表在《文物}1979年第 12期。
# 但文章不仅盲从李膦，对此集大加赞赏 ，而且有造假
# 行为。1983年 ，徐邦达先生撰有《僧原济生卒年岁新
# 订及其它》②，他随从李从汪，文中亦有不妥之处。为
# 此，本文在评此集有关石涛诗文中的几点舛误之时，
# 兼与汪 、徐等先生商榷，并求教于行家。
# 一
# 、 记 录石 涛生年年岁有矛盾
# 在《虬峰文集》有关石涛的诗文中，笔者发现李膦
# 在记录石涛生年年 岁方面有矛盾 ，主要 表现在 以下三
# 个方面 。
# (一 )记 录石 涛生年 为 1642年
# 在李膦《虬峰文集》卷九 ，录有其《清湘子六十赋
# 赠》七律二首，诗云：
# 清湘仙客隐河滨 ，筇杖初 扶指使辰。
# 耆 旧天潢留一老 ，丹青神 品足千春。
# 名登 玉牒伤 孩抱 ，迹托 黄冠避劫尘。
# 沧海纵教 深复浅 ，碧 筒常醉莫辞 频。
# 艺术探 索 2011年 第 25卷 第 2期 总第 105期
# 神交 自昔老招寻 ，手握 心倾喜 不禁。
# 三绝画 图频拜赐 ，五言诗 句每联吟 。
# 悬弧怜我年逢 甲，出腋知君岁在 壬。
# 俱是烈皇 宵旰 日，只今追忆感 弥深。
# 在这里 ，李膦说 他生于 “甲”年 ，石涛 生于“壬”年。
# 此是李膦“只今追忆”所赋。此“甲”年为崇祯七年 甲
# 戌 ；此“壬”年为崇祯十五年壬午。今人汪世清和徐邦
# 达两先生在各 自之文中 算出此诗作于康熙四十年
# 辛巳(1701年)，从辛 巳上推 60年，他们认为正好为壬
# 午年，因而，推定石涛生于明崇祯十五年壬午(1642
# 年 )五月或六月。
# (二 )记 录石涛生年为 1643年
# 在李膦《虬峰文集》卷十六 ，录有其《大涤子传(有
# 序)》，其中写道 ：“大涤子者，原济其名 ，字石涛 ，出自
# 靖江王守谦之后 。守谦 ，高皇帝 之从孙也 ，洪武三年封
# 靖江王 ，国于桂林 。传至 明季 ，南京失守 。王亨嘉 以唐
# 藩序不当立 ，不受诏。两广总制丁魁楚檄思参将陈邦
# 传率兵攻破之 ，执至闽，废为庶人，幽死。是时大涤子
# 生始二岁 ，……”
# 据《明史》、徐燕《小腆纪年附考》、雷亮功《桂林 田
# 海记》、李本天根氏《爝火录》、王夫之《永历实录》《临
# 桂县志》等十多种史料记载 ，朱亨嘉在桂林称监国时
# 为顺治二年 (隆武元年)八月六 日，被擒时为当年
# (1645年 )十二 月二 十二 日。按 李瞵 所说 ，此 时石 涛
# “生始二岁 ”，上推二年 ，石涛生年应为 1643年 。
# (三)记 录石涛生年 为 1636年
# 李膦在《大涤子传(有序 )》中还说 ：“辟黄蘖道场
# 于敬亭之广教寺而居焉，自称小乘客 ，是时年三十矣。
# … … 在敬亭居十有五年，……”此是说 ，石涛开辟黄蘖
# 道场 于敬亭之广教寺而居 ，当时年 30岁。那 么 ，石涛
# 是何时到敬亭广教寺开辟黄蘖道场呢?据笔者查，清
# 宣统元年(1909年 )乌程庞氏刊本庞元济辑《虚斋名
# 画录》卷六 ，著录有《石涛山水书画卷》④。石涛在 自题
# 五律七首末 款署 ：“庚 申闰八月初得 长干一枝七首 ，清
# 湘石涛济山僧又画。”此“庚申”为康熙十九年(1680
# 年)，这说明石涛是年闰八月已离开敬亭广教寺并定
# 居南京长干一枝阁。李膦 自己也说石涛“在敬亭居十
# 有五年”，从 1680年闰八月上推 15年，大概是 1665
# 年之后。目前 ，美术史论界基本公认石涛于 1666年
# 11月就到敬亭广教寺而居。如果 1666年 11月石涛
# “辟黄蘖道场于敬亭之广教寺”而居时是 30岁 ，那么，
# 从 1666年上 推 3O年 ，石涛生年 即是 1636年 。
# 从 以上可 以看出 ，同在一本 文集里 ，李瞵 记录石
# 涛生年年岁就有三种 ，分别为 1642年 、1643年 、1636
# 年。这说明李膦在记录石涛生年年岁方面的思路是
# 十分紊乱不清的。三者之中，无论采用哪一种都会与
# 其他两种有矛盾、有冲突 ；三者同时采用更行不通 。
# 那么，有没有其中一种是正确的呢?笔者认为没有 !
# 因为 ，这三种不仅与石涛 50岁时所作 的“诸方乞食
# 苦瓜僧 ，戒行全无趋小乘。五十孤行成独往，一身禅
# 病冷如冰”⑤之诗所言不相符 ，而且 ，与石涛 60岁生
# 日所作 的《庚辰除夜诗》⑥中所言“今周花甲”亦不相
# 符。所以，李膦《虬峰文集》所记石涛生年年岁是不可
# 信的。
# 可是，汪世清先生和徐邦达先生十分相信和推崇
# 李膦的“壬午说”。为证实此说正确 ，他们认为李诗作
# 于康熙四十年辛 巳(1701年 )，从辛 巳上推 6O年正好
# 为壬午年。其实 ，从辛 巳上推 60年，根本就不是壬午
# 年 ，而应该 同样是辛 巳年。故汪 、徐两位先生的推算 有
# 误 。当汪世清先生发 现“是时大涤子生始二岁 ”与“壬
# 午说”不相符时，却怪镌刻者刻错字 ，说“二疑四之误
# 植”。可是 ，若按“壬午说”，即使到了 1645年 ，石涛也
# 只有三岁，何来 四岁呢?于是韩林德在《石涛与(画语
# 录)研究》中认为“‘二 ’疑为 ‘三 ’之误”。⑦一个说是
# “四”，另一个说是“三”，读者读后不知从谁。但无论怎
# 么改 ，也很难接 近石涛“龆龀遭险难 ”开始换牙 时的岁
# 数。当发现“辟黄蘖道场于敬亭之广教寺而居焉，自称
# 小乘客 ，是时年三十矣”与“壬午说”有冲突时，汪世清
# 先生在其《石涛诗录·石涛东下后的艺术活动年表》中，
# 既承认李膦说石涛“在敬亭居十有五年”，又在后文把
# 石涛安排在“康熙十年辛亥(1671年 )三十岁”，“偕喝
# 涛定居敬亭山广教寺”。他以为这样做似乎可以化解
# 这一冲突 ，既能讲明石涛“在敬亭居十有五年”，又能
# 解决石涛 30岁时确实“辟黄蘖道场于敬亭之广教寺而
# 居焉”。可这样 的记 录前后是矛盾 的。因为 ，从 1671年
# 至 1680年闰八月，石涛在敬亭山广教寺定居不足九年，
# 这显然与李膦在《大涤子传(有序 )》中所说石涛“在敬
# 亭居十有五年”是完全不相符的。汪世清先生却视而
# 不见。当发现“壬午说”与石涛 自己所说在康熙三十九
# 年庚辰(1700年)除夜是他的“今周花甲”不相符时，汪
# 世清先生又想出了讳“九”进十的绝招，以圆其说。于
# 是，他故意歪曲石涛《庚辰除夜诗》所言“今周花甲”是
# 评《虬峰文集》中有关石涛诗文的几点舛误——兼与汪世清、徐邦达等先生商榷 ／陈国平
# “显指新年 辛 巳(1701年 )才届六 十 ，而 庚辰实 为五 十
# 九。”请问：一个人 59岁时能称得上“周花甲”?为什么
# 在对待石涛所言“今周花甲”时用讳“九”进十的方法
# 来解释石涛的年岁，而对待李膦《清湘子六十赋赠》时
# 就不用这种方法来解释石 涛的年岁了呢?这是让 人不
# 可理解的。还有 ，他为了进一步说明“壬午说”是正确
# 的，而石涛《庚辰除夜诗》的“今周花甲”实为 59岁 ，又
# 在其《石涛散考》和《石涛诗录》⑧中，认为石涛在《庚辰
# 除夜诗》中说的“五十有余枝叶少 ，一生累及友朋多”，
# “更是庚辰未六十的内证”。其实，石涛是在回忆 ，他在
# 五十多岁 的时候身体就已经欠佳了，“一生累及 ”就是
# 为“友朋多”而奔波的缘故。即使 由再高明的语言学家
# 来解读 ，这句诗也是这种意思，而根本不是“庚辰未六
# 十”之意。汪世清先生如此牵强，笔者认为是很不妥当
# 的。而徐邦达先生在其文中完全赞同汪世清先生上述
# 的一切说法 ，亦是不妥 当的。
# 而今 ，李膦《虬峰文集》中有关石涛生年年岁的错
# 误记 录 已经 对后人 贻误不 浅 ，加上 汪 、徐 两位 先生 的
# 推波助澜 ，由“壬 午说 ”派生 出来 的 “石 涛生 年为 1642
# 年说”几乎 占领了美术史论界的整个舞台，由此误导
# 了成千上万的人 ，这是需要更正过来 的。
# 其实 ，这里 涉及 如何确定石涛 生年的问题 。在确
# 定石涛生年之前，我们必须明白考证的内涵。考证 ，即
# 考据，要我们根据事实的考核和例证的归纳 ，提供可
# 信的材料，最后作出一定的结论。考证应该是严肃的，
# 实事求是的，一般以第一手和直接材料为主要证据 ，
# 而以第二手和间接材料为次要证据。李膦在《虬峰文
# 集》所说正是第二手材料和间接材料。在石涛生年问
# 题上，他的说法与石涛本人的说法完全不相符 ，甚至
# 还有三个 生年 ，事实证 明又全是错误 的。而汪世清和
# 徐邦达两位先生还盲 目采用，大力推崇 ，还对石涛 自
# 己所说的“庚辰除夜”及“今周花甲”故意歪曲，这种做
# 法是很不妥当的。
# 关于石涛生年，石涛早在 50岁时所作“诸方乞食
# 苦瓜僧 ，戒行 全无趋 小乘 。五十孤行 成独 往 ，一 身禅
# 病冷如冰”之诗及在 60岁所作《庚辰除夜诗》中早就
# 明明白白地告诉 了我们 ，他的出生 日是 ：明崇祯十三
# 年庚辰(1640年)除夜 。用这一出生 日来研究石涛生
# 平 ，写到哪里都不会出错 ，也不必狡辩 ，岂不妙哉 !为
# 何偏要采用错误的证据来确定石涛生年呢?这是很
# 不符合 常理 的。
# 二、记 录石 涛始入佛 门地点 有误
# 李膦在《大涤子传(有序 )》中记载：“传至明季 ，南
# 京失守。王亨嘉以唐藩序不当立 ，不受诏。两广总制丁
# 魁楚檄思参将陈邦传率兵攻破之，执至闽，废为庶人，
# 幽死。是时大涤子生始二岁，为宫中仆臣负出，逃至武
# 昌，剃发为僧。”这里所说石涛始人佛门是在武昌。然
# 其由来究不能悉。但笔者经对石涛二十多年的研究，
# 得知石涛始入佛门根本不是在武昌，而是在广西全州
# 报恩光孝寺(湘山寺 )，兹有据可证 ：
# (一)在汪绎辰辑《大涤子题画诗跋》⑨中，有石涛
# 《今古报恩好》七律一首 ：“瑶天拉眼报恩行 ，光覆浮
# 屠白夜明。借问市朝离乱后 ，几时再感梵王城。”此“报
# 恩 ”之 地 ，是 指南京 报恩寺 ，也可指 全州报 恩光孝 寺 。
# 查黄志璋纂修的《全州志》康熙二十八年抄本④，在清
# 代以前叫“报恩光孝寺 ”的即湘山寺 ，是无量寿佛道
# 场，唐宪宗元和二年建 ，宋高宗绍兴十三年赐额 ，明
# 太祖洪武十六年开设僧正司。在《全县志》⑩中，“报恩
# 光 孝寺”已记为 “湘 山寺”。何 时而 改 ，不得而知 。从 诗
# 中可 以得 知 ，石 涛始入佛 门就是 在广 西全州 报恩 光
# 孝寺 (湘山寺 )。
# (二 )据清代诗人屈大均题石涛《种松图小像》作
# 《石公种松歌》云 ：“师本全州清净禅，湘山湘水别多
