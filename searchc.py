# 三 、记录石涛云游访道行踪有误
# 李鳞在《大涤子传(有序 )》中关于石涛云游访道
# 行踪的记载是这样的：“既而从武昌道荆 门，过洞庭 ，
# 径长沙至衡阳而返。”这些记载 ，与石涛在云游时所
# 作画上留下的游踪线路之事实根本不相符 ，兹有据
# 为证 ：
# (一)石涛云游访道从何地开始?这是首先要弄清
# 楚的问题。前已述及，石涛在全州湘山寺始入佛门，其
# 云游访道，也必然从全州湘山寺开始 ，而根本不可能
# 从武 昌开始 。如果石 涛从 两岁始在武 昌剃发 为僧 ，李
# 膦还说 “年十岁”在武 昌学画，那么 ，石涛又何 以能在
# 湘山寺后飞来石上留下《石刻兰花》的手迹呢?因此，
# 石涛云游访道应从全州湘山寺开始。据清代画家梅清
# 《天延阁删后诗》(康熙辛亥刊本)卷十二《赠喝涛》诗
# 云：“喝公性寡谐 ，远挟爱弟游。出险胆不惊，渺然成双
# 修 。朝泛湘江涯 ，暮陟匡庐陬。”这里说喝涛和石涛入
# 了佛 门后 ，最早云游访道从 “湘江涯 ”开始 。“湘江涯 ”
# 即“湘江涯涣”，是湘江发源地。湘江是湖南省最大河
# 流，其上游海洋河发源地出广西东北海洋山西麓，同
# 漓江间有兴安灵渠相通。其流贯广西东北部、湖南东
# 部，经全州、衡阳、衡山、株州、湘潭、长沙等市县到湘
# 阴县芦林潭人洞庭湖 ，长 817km。既然石涛最早云游
# 访道是从 “湘江 涯”开始 ，他在全 州湘 山寺 为僧 ，必然
# 从全州开始云游访道的。邵松年《古缘萃录》⑩卷六辑
# 有瓶麓斋旧藏《石涛和尚诗画稿卷》④，上有多人题诗
# 共 13首。第一首是石涛《南归赋别金台诸公》，诗中讲
# 到他云游访道的大概线路 ，他说 ：“吾身本蚁寄 ，动作
# 长远游。一行人楚水，再行入吴邱。”何谓‘‘楚水”?按 ：
# 春秋战国时，湖南为楚国地。明时，湖广是楚地。由于
# 湘江流经楚地 ，故 又称 湘江为楚水 。所 以石涛说他 远
# 游一 开始就 “一 行人楚水 ”，即从 全州 出发 ，沿 着楚 水
# 东下 ，后来到了吴越地即江浙一带云游访道 ，最后在
# 松江九峰拜师证道 。以上说 明 ，石涛云游访道是从全
# 州开始 的。
